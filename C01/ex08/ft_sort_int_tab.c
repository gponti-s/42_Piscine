void	ft_sort_int_tab(int *tab, int size)
{
	int n;
	int temp;

	n = 0;
	while (n < (size - 1))
	{
		if (*(tab + n) > *(tab + (n + 1)))
		{
			temp = *(tab + n);
			*(tab + n) = *(tab + (n + 1));
			*(tab + (n + 1)) = temp;
			n = 0;
		}
		else
		{
			n++;
		}
	}
}
