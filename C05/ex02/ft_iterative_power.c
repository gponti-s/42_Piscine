#include <stdio.h>

int		ft_iterative_power(int nb, int power)
{
	int c;

	c = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (power > 1)
	{
		nb = nb * c;
		power--;
	}
	return (nb);
}
