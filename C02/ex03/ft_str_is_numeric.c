int		ft_str_is_numeric(char *str)
{
	int		n;
	int		r;

	n = 0;
	if (str[n] == 0)
	{
		r = 1;
	}
	else
	{
		while (str[n] != '\0')
		{
			if (str[n] >= 48 && str[n] <= 57)
			{
				r = 1;
				n++;
			}
			else
			{
				r = 0;
				break ;
			}
		}
	}
	return (r);
}
