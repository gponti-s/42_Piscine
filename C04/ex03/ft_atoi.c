#include <stdio.h>
#include <stdlib.h>

int		ft_atoi(char *str)
{
	int i;
	int s;

	s = 1;
	i = 0;
	if (*str != '\0')
	{
		while ((*str == '\t') || (*str == '\n') ||
			(*str == '\v') || (*str == '\f') || (*str == '\r') || (*str == ' '))
			str++;
		while (*str == '-' || *str == '+')
		{
			if (*str == '-')
				s *= (-1);
			str++;
		}
		while (*str >= '0' && *str <= '9')
		{
			i = i * 10 + ((*str) - '0');
			str++;
		}
	}
	return (i * s);
}
